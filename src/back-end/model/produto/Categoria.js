class Categoria {
    constructor() {
        this.id = this.id;
        this.nome = this.nome;
    }

    setId(id) {
        this.id = id;
    }

    getId() {
        return this.id;
    }

    setNome(nome) {
        this.nome = nome;
    }

    getNome() {
        return this.nome;
    }
}

export default Categoria;