class Cidade {
    constructor(id, nome, estado) {
        this.id = id;
        this.nome = nome;
        this.estado = estado;
    }

    setId() {
        return this.id;
    }

    getId() {
        return this.id;
    }

    setNome() {
        return this.nome;
    }

    getNome() {
        return this.nome;
    }

    setEstadoId() {
        return this.estado;
    }

    getEstadoId() {
        return this.estado;
    }
}

export default Cidade;